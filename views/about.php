                <h3>About Us</h3>
                <p class="fs-5">
                    Selamat datang di Motor Legends Club, komunitas motor yang telah menjadi pelopor dalam dunia otomotif sejak didirikan pada tahun 2010. Klub kami lahir dari semangat dan kecintaan yang mendalam terhadap motor, dengan tujuan utama untuk menciptakan wadah yang menyatukan para penggemar motor dari berbagai latar belakang.</p>

                <h3>Sejarah Kami</h3>

                <p class="fs-5">Motor Legends Club dimulai sebagai kelompok kecil penggemar motor yang berkumpul untuk berbagi pengetahuan dan pengalaman mereka tentang dunia motor. Dengan semangat kolaborasi dan hasrat yang sama, kami mulai mengorganisir berbagai kegiatan, seperti ride-out dan workshop, yang dirancang untuk meningkatkan keterampilan berkendara dan mempererat hubungan antar anggota. Seiring berjalannya waktu, klub kami berkembang pesat dan kini memiliki lebih dari 500 anggota yang aktif di seluruh negeri.
